import { WebClient } from '@slack/web-api';

let slackClient: WebClient | null = null;

/**
 * Get or create the Slack WebClient instance.
 */
const getSlackClient = (): WebClient | null => {
    if (!process.env.SLACK_BOT_TOKEN) {
        console.warn('Slack bot token not configured. Slack notifications disabled.');
        return null;
    }

    if (!slackClient) {
        slackClient = new WebClient(process.env.SLACK_BOT_TOKEN);
    }
    return slackClient;
};

/**
 * Send a rich Slack notification with Block Kit formatting.
 */
const sendMessage = async (blocks: any[], text: string): Promise<boolean> => {
    const client = getSlackClient();
    if (!client || !process.env.SLACK_CHANNEL_ID) {
        console.log('[Slack] Skipped notification (not configured):', text);
        return false;
    }

    try {
        await client.chat.postMessage({
            channel: process.env.SLACK_CHANNEL_ID,
            text, // Fallback text
            blocks,
        });
        return true;
    } catch (error) {
        console.error('[Slack] Failed to send message:', error);
        return false;
    }
};

/**
 * Notify: SRS document has been generated.
 */
export const notifySRSGenerated = async (projectName: string, srsTitle: string) => {
    return sendMessage(
        [
            {
                type: 'header',
                text: { type: 'plain_text', text: '📄 SRS Document Generated', emoji: true },
            },
            {
                type: 'section',
                fields: [
                    { type: 'mrkdwn', text: `*Project:*\n${projectName}` },
                    { type: 'mrkdwn', text: `*SRS Title:*\n${srsTitle}` },
                ],
            },
            {
                type: 'context',
                elements: [
                    { type: 'mrkdwn', text: '🤖 Generated by *AIVA Agent 1 — Business Analyst*' },
                ],
            },
        ],
        `📄 SRS "${srsTitle}" generated for project "${projectName}"`
    );
};

