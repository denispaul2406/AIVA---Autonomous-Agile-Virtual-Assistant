/**
 * Notify: Meeting has been scheduled with Google Meet link.
 */
export const notifyMeetingScheduled = async (
    meetingTitle: string,
    date: string,
    meetLink: string,
    agenda: string[]
) => {
    const agendaText = (agenda || []).map((item, i) => `${i + 1}. ${item}`).join('\n');

    return sendMessage(
        [
            {
                type: 'header',
                text: { type: 'plain_text', text: '📅 Meeting Scheduled', emoji: true },
            },
            {
                type: 'section',
                fields: [
                    { type: 'mrkdwn', text: `*Meeting:*\n${meetingTitle}` },
                    { type: 'mrkdwn', text: `*Date:*\n${date}` },
                ],
            },
            {
                type: 'section',
                text: { type: 'mrkdwn', text: `*Agenda:*\n${agendaText}` },
            },
            {
                type: 'actions',
                elements: [
                    {
                        type: 'button',
                        text: { type: 'plain_text', text: '🔗 Join Google Meet', emoji: true },
                        url: meetLink,
                        style: 'primary',
                    },
                ],
            },
            {
                type: 'context',
                elements: [
                    { type: 'mrkdwn', text: '🤖 Scheduled by *AIVA Agent 2 — Coordinator*' },
                ],
            },
        ],
        `📅 Meeting "${meetingTitle}" scheduled for ${date}. Join: ${meetLink}`
    );
};

